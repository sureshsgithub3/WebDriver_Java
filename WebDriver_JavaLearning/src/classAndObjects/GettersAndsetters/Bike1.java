package classAndObjects.GettersAndsetters;

public class Bike1 {
	
	int id;
	String model;
	String maker;
	
	
	public void setid(int id){
		this.id=id;
		
	}
	
	public int getid(){
		return id;
	}
	
	public void setmodel(String model){
		this.model=model;
	}
	
	public String getmodel(){
		return model;
	}
	
	public void setMaker(String Maker){
		this.maker=Maker;
	}
	
	public String getmaker(){
		return maker;
	}

}
