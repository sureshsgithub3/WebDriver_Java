package AccessModifiers.ProtectedAccess;

public class StudentProtected {
	
	protected int id;
	protected String name;
	protected int getId() {
		return id;
	}
	protected void setId(int id) {
		this.id = id;
	}
	protected String getName() {
		return name;
	}
	protected void setName(String name) {
		this.name = name;
	}
	

}
